//In programming, two common types of errors are:
// Syntax Error
// A syntax error occurs when the code written in a programming language violates the language's rules and syntax. It's like a grammatical error in a sentence. The code is not valid, and the compiler or interpreter cannot understand it.
// Examples of syntax errors:
// - Missing or mismatched brackets, parentheses, or quotes
// - Using a keyword as a variable name
// - Forgetting to declare a variable or function
// - Using an invalid or unsupported syntax
// Type Error
// A type error occurs when the data type of a value is not compatible with the expected type. This can happen when:
// - Assigning a value of the wrong type to a variable
// - Passing a value of the wrong type to a function
// - Returning a value of the wrong type from a function
// - Using an operator with values of incompatible types
// Examples of type errors:
// - Assigning a string value to a variable declared as a number
// - Passing a boolean value to a function that expects a string
// - Returning an object from a function that is declared to return a number
